-- ============================================
-- 五、创建视图方便查询
-- ============================================

-- 视图1: 只换不修规则完整视图
CREATE VIEW v_damage_replace_rules AS
SELECT 
    dr.id,
    dr.damage_location,
    dr.damage_type,
    dr.severity,
    dr.related_parts,
    dr.is_safety_part,
    dr.replace_reason,
    CASE 
        WHEN dr.is_safety_part = 1 THEN '安全件-必须更换'
        WHEN dr.severity = '严重' THEN '严重损伤-建议更换'
        ELSE '视情况维修或更换'
    END as decision
FROM damage_rules dr;

