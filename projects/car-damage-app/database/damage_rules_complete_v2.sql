-- ============================================
-- 六、创建视图
-- ============================================

CREATE VIEW IF NOT EXISTS v_damage_rules AS
SELECT 
    id,
    accident_type,
    damage_location,
    damage_type,
    severity,
    related_parts,
    is_safety_part,
    replace_rule,
    repair_suggest,
    CASE 
        WHEN is_safety_part = 1 THEN '安全件-必须更换'
        WHEN severity = '严重' THEN '严重损伤-建议更换'
        ELSE '轻微损伤-可修复'
    END as decision
FROM damage_rules_complete;

