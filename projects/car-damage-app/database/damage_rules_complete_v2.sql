-- 七、创建索引
-- ============================================

CREATE INDEX IF NOT EXISTS idx_rules_accident ON damage_rules_complete(accident_type);
CREATE INDEX IF NOT EXISTS idx_rules_location ON damage_rules_complete(damage_location);
CREATE INDEX IF NOT EXISTS idx_rules_type ON damage_rules_complete(damage_type);
CREATE INDEX IF NOT EXISTS idx_rules_safety ON damage_rules_complete(is_safety_part);
