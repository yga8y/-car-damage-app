CREATE INDEX IF NOT EXISTS idx_parts_series ON parts_pricing(series);
CREATE INDEX IF NOT EXISTS idx_parts_oe ON parts_pricing(oe_number);
CREATE INDEX IF NOT EXISTS idx_parts_name ON parts_pricing(part_name);
CREATE INDEX IF NOT EXISTS idx_parts_safety ON parts_pricing(is_safety_part);
