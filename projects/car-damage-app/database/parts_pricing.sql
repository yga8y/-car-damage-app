-- ============================================
-- 四、创建视图
-- ============================================

CREATE VIEW IF NOT EXISTS v_parts_pricing AS
SELECT 
    id,
    brand,
    series,
    year_range,
    part_name,
    oe_number,
    install_position,
    factory_price,
    is_safety_part,
    replace_rule,
    category,
    CASE 
        WHEN is_safety_part = 1 THEN '安全件'
        ELSE '普通件'
    END as part_type
FROM parts_pricing;

